package b11654;

import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        // User Input
        char C;

        // Character Input
        C = sc.next().charAt(0);

        // print to ASCII
        System.out.println((int)C);


        sc.close();
    }
}
